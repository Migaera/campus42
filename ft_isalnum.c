#include "libft.h"

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) == 1 || ft_isdigit(c) == 1)
		return (1);
	return (0);
}
int	main(void)
{
	int c;

	c = 'a';
	printf("%d\n", ft_isalnum(c));
	return (0);
}
