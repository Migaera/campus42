#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
		if (s[i] == c)
			return (char *)s;
		return (NULL);
	}
	return (0);
}